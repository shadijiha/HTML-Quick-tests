/**
 * 
 * @param {number} a First number to be added 
 * @param {number} b Second number to be added
 */

function sum(a, b)  {
    return a + b;
}

/**
 * 
 * @param {number} x First number to be mutiplied 
 * @param {number} y Second number to be mutiplied
 */
function multiply(x, y) {
    return x * y;    
}


